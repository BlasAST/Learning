#include <stdlib.h>
#include <unistd.h>

int main(int argn, char **args)
{
	int	i;

	i = 0;
	if (argn == 2)
	{
		while (args[1][i])
		{
			if (args[1][i] >= 'A' && args[1][i] <= 'Z')
			{
				write (1, "_", 1);
				args[1][i] += 32;
			}
			write (1, &args[1][i], 1);
			i++;
		}
	}	
}
